using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using OrdinarioProyecto.Models;
//creacion ddl controlador de proveedores
namespace OrdinarioProyecto.Controllers
{
    //creacio de la clase de nuestro controlador
    public class ProveedoresController : Controller
    {
        private readonly ProyectoContext _context;
        //Clase que nos permite utilizar context
        public ProveedoresController(ProyectoContext context)
        {
            _context = context;
        }
        //Accion que me permite direccionarme al documento index
        // GET: Proveedores
        public async Task<IActionResult> Index()
        {
              return View(await _context.Proveedores.ToListAsync());
        }
        //Accion que nos ayuda a ver los detalles de su persona especifica
        // GET: Proveedores/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null || _context.Proveedores == null)
            {
                return NotFound();
            }

            var proveedore = await _context.Proveedores
                .FirstOrDefaultAsync(m => m.Id == id);
            if (proveedore == null)
            {
                return NotFound();
            }

            return View(proveedore);
        }
        //Accion que nos manda a la pagina crear para crear un proveedor
        // GET: Proveedores/Create
        public IActionResult Create()
        {
            return View();
        }
        //Accion que nos ayuda a crear nuestro proveedor
        // POST: Proveedores/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("Id,Nombre,Email,Direccion,Telefono")] Proveedore proveedore)
        {
            if (ModelState.IsValid)
            {
                _context.Add(proveedore);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            return View(proveedore);
        }
        //Accion que nos ayuda a dar direccion y modificacion de nuestro proveedor
        // GET: Proveedores/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null || _context.Proveedores == null)
            {
                return NotFound();
            }

            var proveedore = await _context.Proveedores.FindAsync(id);
            if (proveedore == null)
            {
                return NotFound();
            }
            return View(proveedore);
        }
        //Accion que nos ayuda a editar nuestro proveedor
        // POST: Proveedores/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("Id,Nombre,Email,Direccion,Telefono")] Proveedore proveedore)
        {
            if (id != proveedore.Id)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(proveedore);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!ProveedoreExists(proveedore.Id))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(proveedore);
        }
        //Accion que nos permite eliminar nuestro proveedor
        // GET: Proveedores/Delete/5
        public async Task<IActionResult> Delete(int? id)
        {
            if (id == null || _context.Proveedores == null)
            {
                return NotFound();
            }

            var proveedore = await _context.Proveedores
                .FirstOrDefaultAsync(m => m.Id == id);
            if (proveedore == null)
            {
                return NotFound();
            }

            return View(proveedore);
        }
        //Accion que nos ayuda a confirmar, si se quiere eliminar el proveedor
        // POST: Proveedores/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(int id)
        {
            if (_context.Proveedores == null)
            {
                return Problem("Entity set 'ProyectoContext.Proveedores'  is null.");
            }
            var proveedore = await _context.Proveedores.FindAsync(id);
            if (proveedore != null)
            {
                _context.Proveedores.Remove(proveedore);
            }
            
            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Index));
        }
        //Accion que nos permite verificar si nuestro proveedor existe
        private bool ProveedoreExists(int id)
        {
          return _context.Proveedores.Any(e => e.Id == id);
        }
    }
}
